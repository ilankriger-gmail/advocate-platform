export function Sidebar({ isOpen = true, onClose }: SidebarProps) {
  const pathname = usePathname();
  const { user } = useAuth();

  // Verifica se e o criador da comunidade
  const isCreator = user?.user_metadata?.is_creator === true;

  return (
    <>
      {/* Overlay para mobile */}
      {isOpen && (
        <div
          className="fixed inset-0 bg-black/50 z-40 lg:hidden"
          onClick={onClose}
        />
      )}

      {/* Sidebar */}
      <aside
        className={cn(
          'fixed top-14 left-0 z-40 h-[calc(100vh-56px)] transition-transform bg-white border-r border-gray-200',
          'w-64 lg:translate-x-0',
          isOpen ? 'translate-x-0' : '-translate-x-full'
        )}
      >
        <div className="h-full px-3 py-4 overflow-y-auto">
          {/* Create Post Button */}
          <Link
            href="/perfil/novo-post"
            onClick={onClose}
            className="flex items-center justify-center gap-2 w-full px-4 py-3 mb-4 bg-indigo-600 text-white rounded-lg hover:bg-indigo-700 transition-colors font-medium"
          >
            {icons.Plus}
            Criar Post
          </Link>

          {/* Main Navigation */}
          <nav className="space-y-1">
            {MAIN_NAV.map((item) => {
              const isActive = pathname === item.href ||
                (item.href !== '/' && pathname.startsWith(`${item.href}/`));

              return (
                <Link
                  key={item.href}
                  href={item.href}
                  onClick={onClose}
                  className={cn(
                    'flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors',
                    isActive
                      ? 'bg-indigo-50 text-indigo-700'
                      : 'text-gray-700 hover:bg-gray-100'
                  )}
                >
                  <span className={isActive ? 'text-indigo-600' : 'text-gray-500'}>
                    {icons[item.icon]}
                  </span>
                  {item.label}
                </Link>
              );
            })}
          </nav>

          {/* Creator Navigation */}
          {isCreator && (
            <>
              <hr className="my-4 border-gray-200" />
              <p className="px-3 mb-2 text-xs font-semibold text-gray-500 uppercase tracking-wider">
                Criador
              </p>
              <nav className="space-y-1">
                {CREATOR_NAV.map((item) => {
                  const isActive = pathname === item.href || pathname.startsWith(`${item.href}/`);

                  return (
                    <Link
                      key={item.href}
                      href={item.href}
                      onClick={onClose}
                      className={cn(
                        'flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors',
                        isActive
                          ? 'bg-indigo-50 text-indigo-700'
                          : 'text-gray-700 hover:bg-gray-100'
                      )}
                    >
                      <span className={isActive ? 'text-indigo-600' : 'text-gray-500'}>
                        {icons[item.icon]}
                      </span>
                      {item.label}
                    </Link>
                  );
                })}
              </nav>
            </>
          )}

          {/* Footer */}
          <div className="absolute bottom-0 left-0 right-0 p-4 border-t border-gray-200 bg-gray-50">
            <div className="flex items-center gap-2 text-xs text-gray-500">
              <span className="w-2 h-2 bg-green-500 rounded-full" />
              Conectado
            </div>
          </div>
        </div>
      </aside>
    </>
  );
}
